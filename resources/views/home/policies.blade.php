                <h1>CSADI FRAUD POLICY</h1>
                <hr style="border: 1px solid grey">
                <p>
                <h3>BACKGROUND</h3>
                CSADI fraud policy is established to facilitate the development of controls that will aid in the
                detection and prevention of fraud against CSADI. It is the intent of CSADI to promote consistent
                organizational behaviour by providing guidelines and assigning responsibility for the development of
                controls and conduct of investigations.
                </p>
                <p>
                <h3>SCOPE OF POLICY</h3>
                This policy applies to any irregularity, or suspected irregularity, involving employees as well as
                volunteers, consultants, vendors, contractors, outside agencies doing business with employees of such
                agencies, and/or any other parties with a business relationship with CSADI. Any investigative activity
                required will be conducted without regard to the suspected wrongdoer’s length of service,
                position/title, or relationship to the Organisation.
                </p>
                <p>
                <h3>POLICY</h3>
                Management is responsible for the detection and prevention of fraud, misappropriations, and other
                irregularities. Fraud is defined as the intentional, false representation or concealment of a material
                fact for the purpose of inducing another to act upon it to his or her injury. Each member of the
                management team will be familiar with the types of improprieties that might occur within his or her area
                of responsibility, and be alert for any indication of irregularity. Any irregularity that is detected or
                suspected must be reported immediately to the Human resources, who coordinates all investigations with
                the Legal Department and other affected areas, both internal and external.
                </p>
                <p>
                <h3>ACTIONS CONSTITUTING FRAUD</h3>
                The terms defalcation, misappropriation, and other fiscal irregularities refer to, but are not limited
                to:
                i. Any dishonest or fraudulent act
                ii. Misappropriation of funds, securities, supplies, or other assets
                iii. Impropriety in the handling or reporting of money or financial transactions
                iv. Profiteering as a result of insider knowledge of organisational activities
                v. Disclosing confidential and proprietary information to outside parties
                vi. Disclosing to other persons securities activities engaged in or contemplated by the organisation
                vii. Accepting or seeking anything of material value from contractors, vendors, or persons providing
                services/materials to the organisation. Exception: Gifts less than $50 in value.
                viii. Destruction, removal, or inappropriate use of records, furniture, fixtures, and equipment; and/or
                ix. Any similar or related irregularity
                </p>
                <p>
                <h3>OTHER IRREGULARITIES</h3>
                Irregularities concerning an employee’s moral, ethical, or behavioural conduct should be resolved by
                departmental management and the Employee Relations Unit of Human Resources. If there is any question as
                to whether an action constitutes fraud, contact the Human resource manager for guidance.
                INVESTIGATION RESPONSIBILITIES
                The human resource Unit has the primary responsibility for the investigation of all suspected fraudulent
                acts as defined in the policy. If the investigation substantiates that fraudulent activities have
                occurred, the human resource Unit will issue reports to appropriate designated personnel and, if
                appropriate, to the Board of Directors through the Audit Committee. Decisions to prosecute or refer the
                examination results to the appropriate law enforcement and/or regulatory agencies for independent
                investigation will be made in conjunction with legal counsel and senior management, as will final
                decisions on disposition of the case.

                </p>
                <p>
                <h3>CONFIDENTIALITY</h3>
                The Human resources Unit treats all information received confidentially. Any employee who suspects
                dishonest or fraudulent activity will notify the HR Unit immediately, and should not attempt to
                personally conduct investigations or interviews/interrogations related to any suspected fraudulent act.
                Investigation results will not be disclosed or discussed with anyone other than those who have a
                legitimate need to know. This is important in order to avoid damaging the reputations of persons
                suspected but subsequently found innocent of wrongful conduct and to protect the organisation from
                potential civil liability.

                </p>
                <p>
                <h3>AUTHORIZATION FOR INVESTIGATING SUSPECTED FRAUD</h3>
                Members of the Investigation Unit will have:
                i. Free and unrestricted access to all organisations records and premises, whether owned or rented; and
                ii. The authority to examine, copy, and/or remove all or any portion of the contents of files, desks,
                cabinets, and other storage facilities on the premises without prior knowledge or consent of any
                individual who might use or have custody of any such items or facilities when it is within the scope of
                their investigation.

                </p>
                <p>
                <h3>REPORTING PROCEDURES</h3>
                Great care must be taken in the investigation of suspected improprieties or irregularities so as to
                avoid mistaken accusations or alerting suspected individuals that an investigation is under way. An
                employee who discovers or suspects fraudulent activity will contact the HR Unit immediately. The
                employee or other complainant may remain anonymous. All inquiries concerning the activity under
                investigation from the suspected individual, his or her attorney or representative, or any other
                inquirer should be directed to the Investigations Unit or the Legal Department. No information
                concerning the status of an investigation will be given out. The proper response to any inquiries is: “I
                am not at liberty to discuss this matter.” Under no circumstances should any reference be made to “the
                allegation,” “the crime,” “the fraud,” “the forgery,” “the misappropriation,” or any other specific
                reference. The reporting individual should be informed of the following:
                i. Do not contact the suspected individual in an effort to determine facts or demand restitution.
                ii. Do not discuss the case, facts, suspicions, or allegations with anyone unless specifically asked to
                do so by the human resource Department.
                </p>
                <p>
                    <h3>TERMINATION</h3>
                    If an investigation results in a recommendation to terminate an individual, the recommendation will be reviewed for approval by the designated representatives from Human Resource Department and, if necessary, by outside counsel, before any such action is taken. The investigation team does not have the authority to terminate an employee. The decision to terminate an employee is made by the employee’s management. Should the investigative Unit believe the management decision inappropriate for the facts presented, the facts will be presented to executive level management for a decision.
                </p>
                <p>
                    <h3>ADMINISTRATION</h3>
                    The Human resources manager is responsible for the administration, revision, interpretation, and application of this policy. The policy will be reviewed annually and revised as needed.
                </p>
