                        <h3> POLICY STATEMENT</h3>
                        <p>
                            1.1 - ​It is our policy to conduct all of our business in an honest and ethical manner. We
                            take a zero-tolerance approach to Bribery and Corruption and are committed to acting
                            professionally, fairly and with integrity in all our business dealings and relationships
                            wherever we operate and implementing and enforcing effective systems to counter bribery.
                        </p>
                        <p>
                            1.2 - ​We will uphold all laws relevant to countering bribery and corruption in all the
                            jurisdictions in which we operate. We remain bound by local and national laws. [Please check
                            if FCPA and UK Bribery Act apply to your company].
                        </p>
                    </li>
                    <li>
                        <h3>DEFINITIONS</h3>
                        <p>
                            “Agent”: Any individual acting as an agent, paid by the company, acting on the company’s
                            behalf in negotiating with Third Parties.

                            “Bribery” / “Corruption”: Bribery occurs when one person offers, pays, seeks or accepts a
                            payment, gift, favour, or a financial or other advantage from another to influence a
                            business outcome improperly, to induce or reward improper conduct or to gain any commercial,
                            contractual, regulatory or personal advantage. It can be direct or indirect through Third
                            Parties.

                            “Company”: All subsidiaries and affiliated companies.

                            “Conflict of Interest”: Occurs when an individual or organisation is involved in multiple
                            interests, one of which could possibly corrupt, or be perceived to corrupt, the motivation
                            for an act in another.

                            “Donation”: A Donation is a voluntary contribution in the form of monetary or non-monetary
                            gifts to a fund or cause for which no return service or payment is expected or made.
                            Contributions to industry associations or fees for memberships in organisations that serve
                            business interests are not necessarily considered Donations.

                            “Employee”: For the purposes of this policy this includes all individuals working at all
                            levels and grades, including senior managers, officers, directors, employees (whether
                            permanent, fixed-term or temporary), consultants, contractors, trainees, seconded staff,
                            home-workers, casual workers and agency staff, volunteers, interns, agents, sponsors, or any
                            other person associated with us, or any of our subsidiaries or joint ventures or their
                            employees, wherever they are located.

                            “Facilitation Payments”: A form of Bribery in which small payments are made with the purpose
                            of expediting or facilitating the performance by a Public Official of a routine governmental
                            action and not to obtain or retain business or any other undue advantage. Facilitation
                            payments are typically demanded by low level and low income Public Officials in exchange for
                            providing services to which one is legally entitled without such payments.

                            “Gifts, Invitations & Hospitality”: Invitations given or received to social functions,
                            sporting events, meals and entertainment, gifts or customary tokens of appreciation.

                            “Intermediary”: Includes but is not limited to Agents, distributors, consultants, sales
                            representatives, implementation partners, sales partners.

                            “Kickback”: A bribe to obtain an undue advantage, where a portion of the undue advantage is
                            'kicked backed' to the person who gave, or is supposed to give, the undue advantage.

                            “Public Official”: Officials or employees of any government or other public body, agency or
                            legal entity, at any level, including officers or employees of state-owned enterprises and
                            officers or employees of enterprises which are mandated by a public body or a state-owned
                            enterprise to administrate public functions.

                            “Sponsorship”: Sponsorship is about partnering with external organisations to deliver mutual
                            benefits through an exchange of monies, products, services, content or other intellectual
                            property.

                            “Third Party”: Any individual or organisation you come into contact with during the course
                            of your work for us. This includes actual and potential customers, suppliers, business
                            contacts, Intermediaries, government and public bodies, including their advisors,
                            representatives and officials, politicians and political parties.

                        </p>
